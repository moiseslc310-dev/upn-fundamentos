#Ejercicio 1: Pago Total por Alquiler de una Casa

habitacion =float(input('Ingrese el Nro de Habitaciones: '))

bano =float(input('Ingrese el Nro de Baños: '))

patio =float(input('Ingrese el Nro de Patios: '))

total = habitacion*30 + bano*10 + patio*15

print(f'El pago total por alquiler es: {total:.2f}')
